const now = new Date();
console.log(now); //Fri Jul 28 2023 07:46:08 GMT+0700 (GMT+07:00)
console.log(now.getFullYear()); //2023
console.log(now.getDate()); //28
console.log(now.getDay()); // Thứ 6 trả về số: 5
console.log(now.getMonth()); // Tháng 7 trả về số: 6
console.log(now.getTime()); // Trả về giá trị dữ liệu thời gian từ ngày 1/1/1970 đến hiện tại
console.log(now.getTimezoneOffset()); // trả ra một giá trị khác về phút giữa thời gian local và UTC
console.log(now.getUTCFullYear());
